package com.dentalchain.sopix;

/** Protocol observed in the complete Windows USB capture 234.pcapng. */
public final class SopixProtocol {
    public static final class Step {
        public final int delayMs;
        public final byte[] data;
        Step(int delayMs, int... values) {
            this.delayMs = delayMs;
            this.data = new byte[values.length];
            for (int i = 0; i < values.length; i++) this.data[i] = (byte) values[i];
        }
        public int sequence() { return data[0] & 0xff; }
    }

    private SopixProtocol() {}

    // Cold-open initialization and acquisition arming sequence.
    // This is the sequence immediately preceding the successful 2,625,000-byte EP82 image burst.
    public static final Step[] ACQUIRE = new Step[] {
        new Step(0,  0x01,0x84,0x00,0x01,0x00,0x00,0x00,0x00),
        new Step(14, 0x02,0x84,0x00,0x01,0x00,0x00,0x00,0x00),
        new Step(5,  0x03,0x00,0x00,0x00,0x00,0x00,0x00,0x00),
        new Step(1,  0x04,0x88,0x00,0x04,0x00,0x00,0x00,0x00),
        new Step(1,  0x05,0x86,0x01,0x00,0x00,0x00,0x00,0x00),
        new Step(8,  0x06,0x88,0x00,0x04,0x00,0x00,0x00,0x00),
        new Step(1,  0x07,0x86,0x01,0x00,0x00,0x00,0x00,0x00),
        new Step(21, 0x08,0x84,0x00,0x01,0x00,0x00,0x00,0x00),
        new Step(81, 0x09,0x1b,0x00,0x00,0x00,0x00,0x1f,0xff),
        new Step(1,  0x0a,0x03,0x00,0x00,0x00,0x00,0x00,0x00)
    };
}
